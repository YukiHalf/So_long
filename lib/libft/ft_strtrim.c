#include "libft.h"

char	*ft_strtrim(const char *s1, const char *set)
{
	size_t	start;
	size_t	end;
	size_t	trm_len;
	char	*trim;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	trm_len = end - start;
	trim = (char *)malloc(trm_len + 1);
	if (!trim)
		return (NULL);
	ft_memcpy(trim, s1 + start, trm_len + 1);
	trim[trm_len] = '\0';
	return (trim);
}
